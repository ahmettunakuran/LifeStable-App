  static String of(String key, {Map<String, String>? args}) {
    final lang = localeNotifier.value.languageCode;
    String res = _data[lang]?[key] ?? _data['en']?[key] ?? key;
    if (args != null) {
      args.forEach((k, v) {
        res = res.replaceAll('{$k}', v);
      });
    }
    return res;
  }
