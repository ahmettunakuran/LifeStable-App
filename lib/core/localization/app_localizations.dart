class LanguageSwitcher extends StatelessWidget {
  const LanguageSwitcher({super.key});

  @override
  Widget build(BuildContext context) {
    return ValueListenableBuilder<Locale>(
      valueListenable: localeNotifier,
      builder: (context, locale, _) {
        return DropdownButton<String>(
          value: locale.languageCode,
          dropdownColor: const Color(0xFF1A1200),
          underline: const SizedBox(),
          icon: const Icon(Icons.language, color: Color(0xFFFFB300), size: 20),
          items: const [
            DropdownMenuItem(value: 'en', child: Text('English', style: TextStyle(color: Colors.white))),
            DropdownMenuItem(value: 'tr', child: Text('Türkçe', style: TextStyle(color: Colors.white))),
          ],
          onChanged: (val) {
            if (val != null) localeNotifier.value = Locale(val);
          },
        );
      },
    );
  }
}
